select	a11.ID_ANO_LANC  ID_ANO,
	a11.ID_MES_LANC  ID_MES,
	a11.ID_DIA_LANC  ID_DIA,
	max(a110.NO_DIA_COMPLETO)  NO_DIA_COMPLETO,
	a14.ID_ITEM_INFORMACAO  ID_ITEM_INFORMACAO,
	max(a116.NO_ITEM_INFORMACAO)  NO_ITEM_INFORMACAO,
	max(a116.CO_ITEM_INFORMACAO)  CO_ITEM_INFORMACAO,
	a19.ID_FONTE_RECURSO  ID_FONTE_RECURSO,
	max(a115.CO_FONTE_RECURSO)  CO_FONTE_RECURSO,
	max(a115.NO_FONTE_RECURSO)  NO_FONTE_RECURSO,
	a18.ID_ORGAO_MAXI  ID_ORGAO_MAXI,
	max(a114.CO_ORGAO_MAXI)  CO_ORGAO_MAXI,
	max(a114.NO_ORGAO_MAXI)  NO_ORGAO_MAXI,
	a16.ID_ORGAO_UG  ID_ORGAO,
	max(a113.CO_ORGAO)  CO_ORGAO,
	max(a113.NO_ORGAO)  NO_ORGAO,
	a11.ID_VINCULACAO_PAGAMENTO  ID_VINCULACAO_PAGAMENTO,
	max(a112.NO_VINCULACAO_PAGAMENTO)  NO_VINCULACAO_PAGAMENTO,
	a15.ID_DOCUMENTO  ID_DOCUMENTO,
	a15.TX_OBSERVACAO  TX_OBSERVACAO,
	a11.ID_FONTE_RECURSO_DETA  ID_FONTE_RECURSO_DETA,
	max(a19.CO_FONTE_RECURSO_DETA)  CO_FONTE_RECURSO_DETA,
	max(a19.NO_FONTE_RECURSO_DETA)  NO_FONTE_RECURSO_DETA,
	a16.ID_UG  ID_UG_EXEC,
	max(a111.CO_UG)  CO_UG,
	max(a111.NO_UG)  NO_UG,
	sum(((a11.VA_MOVIMENTO_LIQUIDO * a14.IN_OPERACAO_EXPRESSAO) * a13.PE_TAXA))  SALDORITEMINFORMAO
from	MS_33393_DWTG_PRO.WF_LANCAMENTO	a11
	join	MS_33393_DWTG_PRO.WD_MOEDA	a12
	  on 	(a11.ID_MOEDA_UG_EXEC_H = a12.ID_MOEDA)
	join	MS_33393_DWTG_PRO.WD_TAXA_CAMBIO_MENSAL	a13
	  on 	(a12.ID_MOEDA = a13.ID_MOEDA_ORIGEM)
	join	MS_33393_DWTG_PRO.WD_ITEM_DECODIFICADO_CCON	a14
	  on 	(a11.ID_ANO_LANC = a14.ID_ANO_ITEM_CONTA and 
	a11.ID_CONTA_CONTABIL_LANC = a14.ID_CONTA_CONTABIL)
	join	MS_33393_DWTG_PRO.WD_DOCUMENTO	a15
	  on 	(a11.ID_ANO_LANC = a15.ID_ANO_EMISSAO_DOC and 
	a11.ID_DOCUMENTO_LANC = a15.ID_DOCUMENTO)
	join	MS_33393_DWTG_PRO.WD_UG_EXERCICIO	a16
	  on 	(a11.ID_ANO_LANC = a16.ID_ANO and 
	a11.ID_UG_EXEC = a16.ID_UG)
	join	MS_33393_DWTG_PRO.WD_ORGAO_EXERCICIO	a17
	  on 	(a16.ID_ANO = a17.ID_ANO and 
	a16.ID_ORGAO_UG = a17.ID_ORGAO)
	join	MS_33393_DWTG_PRO.WD_ORGAO_SUPE_EXERCICIO	a18
	  on 	(a17.ID_ANO = a18.ID_ANO and 
	a17.ID_ORGAO_SUPE = a18.ID_ORGAO_SUPE)
	join	MS_33393_DWTG_PRO.WD_FONTE_RECURSO_DETA	a19
	  on 	(a11.ID_FONTE_RECURSO_DETA = a19.ID_FONTE_RECURSO_DETA)
	join	MS_33393_DWTG_PRO.WD_DIA	a110
	  on 	(a11.ID_ANO_LANC = a110.ID_ANO and 
	a11.ID_DIA_LANC = a110.ID_DIA and 
	a11.ID_MES_LANC = a110.ID_MES)
	join	MS_33393_DWTG_PRO.WD_UG	a111
	  on 	(a16.ID_UG = a111.ID_UG)
	join	MS_33393_DWTG_PRO.WD_VINCULACAO_PAGAMENTO	a112
	  on 	(a11.ID_VINCULACAO_PAGAMENTO = a112.ID_VINCULACAO_PAGAMENTO)
	join	MS_33393_DWTG_PRO.WD_ORGAO	a113
	  on 	(a16.ID_ORGAO_UG = a113.ID_ORGAO)
	join	MS_33393_DWTG_PRO.WD_ORGAO_MAXI	a114
	  on 	(a18.ID_ORGAO_MAXI = a114.ID_ORGAO_MAXI)
	join	MS_33393_DWTG_PRO.WD_FONTE_RECURSO	a115
	  on 	(a19.ID_FONTE_RECURSO = a115.ID_FONTE_RECURSO)
	join	MS_33393_DWTG_PRO.WD_ITEM_INFORMACAO	a116
	  on 	(a14.ID_ITEM_INFORMACAO = a116.ID_ITEM_INFORMACAO)
where	(a18.ID_ORGAO_MAXI in (30000)
 and a14.ID_ITEM_INFORMACAO in (502)
 and a11.ID_ANO_LANC in (2017, 2018, 2019)
 and a13.ID_ANO = a11.ID_ANO_LANC
 and a13.ID_MES = a11.ID_MES_LANC)
group by	a11.ID_ANO_LANC,
	a11.ID_MES_LANC,
	a11.ID_DIA_LANC,
	a14.ID_ITEM_INFORMACAO,
	a19.ID_FONTE_RECURSO,
	a18.ID_ORGAO_MAXI,
	a16.ID_ORGAO_UG,
	a11.ID_VINCULACAO_PAGAMENTO,
	a15.ID_DOCUMENTO,
	a15.TX_OBSERVACAO,
	a11.ID_FONTE_RECURSO_DETA,
	a16.ID_UG